public class score {

    private int playerRuns;
    private int playerBoundaries;
    private double playerStrikeRate;
    private String playerVenue;
    private String playerImpIncident;


    public int getPlayerRuns() {
        return playerRuns;
    }

    public void setPlayerRuns(int playerRuns) {
        this.playerRuns = playerRuns;
    }

    public int getPlayerBoundaries() {
        return playerBoundaries;
    }

    public void setPlayerBoundaries(int playerBoundaries) {
        this.playerBoundaries = playerBoundaries;
    }

    public double getPlayerStrikeRate() {
        return playerStrikeRate;
    }

    public void setPlayerStrikeRate(double playerStrikeRate) {
        this.playerStrikeRate = playerStrikeRate;
    }

    public String getPlayerVenue() {
        return playerVenue;
    }

    public void setPlayerVenue(String playerVenue) {
        this.playerVenue = playerVenue;
    }

    public String getPlayerImpIncident() {
        return playerImpIncident;
    }

    public void setPlayerImpIncident(String playerImpIncident) {
        this.playerImpIncident = playerImpIncident;
    }


}
